int send_packet(libusb_device_handle *handle, const char *data, int frame_id) {
  if (!data || strlen(data) == 0)
    return 0;
  int length = (frame_id == 111) ? 59 : 567;
  uint16_t wValue = (0x02 << 8) | (unsigned char)data[0];
  int r = libusb_control_transfer(handle, 0x21, 0x09, wValue, INTERFACE_NUM,
                                  (unsigned char *)data, length, 1000);
  if (r >= 0) {
    usleep(15000);
    return 0;
  }
  return -1;
}

void perform_identity_check(libusb_device_handle *handle) {
  unsigned char data[255];
  libusb_get_string_descriptor_ascii(handle, 0, data, sizeof(data));
  libusb_get_string_descriptor_ascii(handle, 1, data, sizeof(data));
  libusb_get_string_descriptor_ascii(handle, 2, data, sizeof(data));
  usleep(15000);
}