#include <unistd.h>

#define INTERFACE_NUM 2
#define ENDPOINT_IN 0x84
#define OUTPUT_FILE "/tmp/ajazz_battery"

libusb_context *ctx = NULL;
int running = 1;

// --- PROTOTYPES ---
int get_battery_from_device(libusb_device_handle *dev_handle);
int send_packet(libusb_device_handle *handle, const char *data, int frame_id);
int read_reply(libusb_device_handle *handle, int timeout_ms);
void perform_identity_check(libusb_device_handle *handle);
void write_status(int bat_level, const char *class_name);
int perform_handshake(libusb_device_handle *handle);

void handle_sig(int sig) { running = 0; }

int main(int argc, char *argv[]) {
  signal(SIGINT, handle_sig);
  signal(SIGTERM, handle_sig);

  uint16_t vendor_id = 0x249a;
  uint16_t product_id = 0x5c2f;

  if (argc >= 3) {
    vendor_id = (uint16_t)strtol(argv[1], NULL, 16);
    product_id = (uint16_t)strtol(argv[2], NULL, 16);
  } else {
    printf("No IDs provided, defaulting to Vendor: %04x, Product: %04x\n", vendor_id, product_id);
  }

  if (libusb_init(&ctx) < 0)
    return 1;

  // Start with blank status
  write_status(-1, "disconnected");

  while (running) {
    // 1. Try to open device
    // If mouse is sleeping (5s rule), this returns NULL immediately.
    libusb_device_handle *handle =
        libusb_open_device_with_vid_pid(ctx, vendor_id, product_id);

    if (handle) {
      // DEVICE FOUND (Mouse is awake!)

      //  Claim Interface
      if (libusb_kernel_driver_active(handle, INTERFACE_NUM) == 1) {
        libusb_detach_kernel_driver(handle, INTERFACE_NUM);
      }
      libusb_claim_interface(handle, INTERFACE_NUM);

      //  Get Battery
      int bat = get_battery_from_device(handle);

      if (bat >= 0) {
        write_status(bat, "mouse");
      }

      //  CLEAN EXIT (Crucial for Sleep Mode)
      // We release everything so the mouse feels free to disconnect/sleep.
      libusb_release_interface(handle, INTERFACE_NUM);
      libusb_attach_kernel_driver(handle, INTERFACE_NUM);
      libusb_close(handle);
    } else {
      // Mouse is sleeping/unplugged.
      // We do NOT write "..." here. We keep the last known battery %
    }

    // Wait 1.5 seconds.
    // This is fast enough to catch movement, but slow enough to save CPU.
    usleep(1500000);
  }

  libusb_exit(ctx);
  return 0;
}

// --- LOGIC: The "Touch & Go" Strategy ---
int get_battery_from_device(libusb_device_handle *handle) {
  const char *query_data =
      "\033\000 "
      "\271\373\256\216\211\377\377\000\000\000\000\t\000\000\001\000\b\000\005"
      "\001 "
      "\000\000\000\020\000\000\000\000\000\000\000\000\000\000\000\000\000\000"
      "\000\000\000\000\000\000\000\000\000\000\000\000\000\000\000\000\000\000"
      "\000";

  libusb_clear_halt(handle, ENDPOINT_IN);

  if (send_packet(handle, query_data, 111) == 0) {
    int bat = read_reply(handle, 200);
    if (bat >= 0)
      return bat;
  }

  if (perform_handshake(handle) == 0) {

    send_packet(handle, query_data, 111);
    return read_reply(handle, 500);
  }

  return -1;
}

int read_reply(libusb_device_handle *handle, int timeout_ms) {
  unsigned char buffer[64] = {0};
  int transferred;
  int r;
  int loops = timeout_ms / 10;

  for (int i = 0; i < loops; i++) {
    r = libusb_interrupt_transfer(handle, ENDPOINT_IN, buffer, 64, &transferred,
                                  10);
    if (r == 0 && transferred > 0) {
      // Filter: Only accept packets starting with C0
      if (buffer[0] == 0xC0)
        return buffer[2];
    }
  }
  return -1;
}

